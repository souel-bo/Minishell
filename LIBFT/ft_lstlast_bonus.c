#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*last_content;

	if (!lst)
		return (NULL);
	last_content = lst;
	while (last_content != NULL && last_content->next != NULL)
	{
		last_content = last_content->next;
	}
	return (last_content);
}
